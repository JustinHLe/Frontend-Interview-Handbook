/*  
    First class functions are functions that can be treated as a variable
    meaning that they can be assigned to variables, passed as parameters, and returned from other functions


    A high order function is a function that receives another function as an argument or returns
    another function or both. 

    A higher order function builds off first class functions meaning that a function is allowed to take
    one or more functions as an argument


    Examples of high order functions in javascript 
    map 
    reduce
    filter
    forEach
    sort
*/