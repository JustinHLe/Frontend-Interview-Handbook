/*
    Var variables are functional scope meaning they are accessible anywhere within the function
    Var variables are hoisted to the top of their scope meaning they can be accessed before initialization
    Var variables can be redeclared


    Let variables are blocked scope meaning they are confined between the brackets
    Let variables are hoisted but will throw a reference error when accessed before initialization because they are not initialized until they're read by the compiler
    Let variables cannot be redeclared

    Const is the exact same as let except it is immutable
*/